	<h1>What is FIRST?</h1>
	<p>FIRST (For the Inspiration and Recognition of Science and Technology) is a program founded by Dean Kamen to inspire students to be leaders in science and technology, innovation, and foster well-rounded life capabilities including self-confidence, communication, and leadership in students as well as build science, engineering, technology and problem solving skills. To learn more about the FIRST organization, click <a href="http://www.usfirst.org/" target="_blank">here</a>.</p>
