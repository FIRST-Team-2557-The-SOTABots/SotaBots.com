	<h1>Current News</h1>
	<h2>Working Hard</h2>
	<p><center><img src="Images/Current News/working hard during microterm.jpg"/></center></p>
	<p>Micro-term started this Monday and we have been working hard to get ready for our summer outreach. We have started planning lessons for our monthly FLL Camps and finding great resources for teaching STEM to elementary schoolers. We are also planning visits to our current sponsors and an FLL workshop before the FLL kick-off. The build team is working on a new drive-train prototype.
Today we also had our first shop fire drill. It was unexpected and a really great way to learn about procedures in the event of an emergency at the school. We are very prepared now!</p>
	<h2>St. Louis</h2>
	<p>We were placed in the Archimedes Division in which we ranked 39<sup>th</sup> with 6 wins and 4 losses. We were chosen by the 2<sup>nd</sup> seed alliance with the captain team being FIRST Team 2907 Lion Robotics. The other teams allied with us were FIRST Team 3393 Horns of Havoc and FIRST Team 4911 CyberKnights. Our all Northwest region alliance pushed our way to the semi-finals and lost to the 3<sup>rd</sup> seed alliance.</p>
	<h2>Packing for St. Louis</h2>
	<p><center><img src="Images/Current News/st.louispacking.jpg"/></center></p>
	<p>In the Portland competition, we were one of twenty-four teams that qualified to go to nationals in St. Louis. We are now packing up with team 3393, Horns of Havoc, so we can compete to the best of our ability. We are excited to compete with 398 teams from all over the world. Our team will be competing on Archimedes field.</p>
