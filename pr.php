<h1 id="PR">Public Relations</h1>
	
	<h2>About the team</h2>
	<p>The SOTABots are a local robotics team combining students from Tacoma School of the Arts, Tacoma Science and Math Institute, and Stewart Middle School. Our team is a part of the FIRST Robotics organization, a worldwide association designed to get young adults involved in engineering. Every year, we design and build several robots to compete in various FIRST challenges.</p>
	<h2>Tacoma Robtics Alliance</h2>
	<p>The TRA is an organization that provides support, and builds community for FIRST teams in the Puget Sound region. It was founded by SOTA and SAMI students whom make up the SOTABots. Yearly, we construct a full size practice field for every FRC and FTC seasons game challenge. The practice field we construct is open to any FIRST team who can come to our location in South Tacoma. Our Practice field enables FRC and FTC team to test and practice with their FIRST robots so they can succeed in competition. </p>
