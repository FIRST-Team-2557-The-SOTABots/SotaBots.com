		<h2>Looking For A Partner?</h2>
		<p>The SOTABots are a robotics team involved in the FIRST Robotics Competition. FIRST (For Inspiration and Recognition of Science and Technology) is a mentor based program. It was founded by renowned inventor Dean Kamen with the goal of transforming our culture by creating a world where science and technology are celebrated, and where young people begin to realize their dream of becoming science and technology leaders.</p>
		<p>SOTABots was started at the Tacoma School of the Arts in 2008 and has since expanded to include team members from not only the Tacoma School of the Arts and their sister school SAMI (Science and Math Institute), but also from Lincoln High School and Stadium High School.</p>
		<p>SOTABots personifies Dean Kamen's vision by mentoring other FIRST teams and giving robotics presentations at local middle schools to get possible future team members excited about robotics and engineering. SOTABots, like all FIRST teams, is not just about building a robot and competing. It is about the life skills students can acquire through the hard work and dedication of creating something while being an integral part of a team.</p>
		<p> Thanks in no small part to our gracious sponsors we have been able to accomplish great things since our inception. In 2008 we achieved the "Rookie of the Year Award" and in 2010 we won the Seattle Microsoft regional competition. However our performance in the tournaments is not our sole focus. SOTABots is dedicated to creating well-rounded individuals who reach out to help other teams and the local community. This has been acknowledged through other honors we have received such the Chrysler Team Spirit Award, The Industrial Safety Award, the Motorola Quality Award, and the Gracious Professionalism Award.</p>
		<p>As a sponsor, your logo will be seen throughout Western Washington and points beyond. Our team apparel is worn not only for competition but throughout the year by students and mentors alike as a badge of pride. At the world competition in St. Louis, April 2014, we were one of 400 teams that qualified to compete. Every year, this organization is growing so we will need sponsors to compete to the best of our abilities. Your tax-deductible donation will not only support the SOTABots, but provide further advertising exposure for your business as well. </p>
		<p>Our team, the life skills it builds, and the honors we've received would never have been possible without the support of sponsors. We would be proud to have you as a sponsor. Any and all sponsorship levels are greatly appreciated. </p>

